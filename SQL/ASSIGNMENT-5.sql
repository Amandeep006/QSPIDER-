WHERE (COMM IS NOT NULL AND DEPTNO=30);

-- 9. WAQTD AVG SAL, TOTAL SAL, NUMBER OF EMPS AND MAXIMUM SALARY GIVEN TO EMPLOYEES WORKING AS PERSIDENT.
SELECT AVG(SAL),SUM(SAL),COUNT(*) AND MAX(SAL) FROM EMP_TABLE
