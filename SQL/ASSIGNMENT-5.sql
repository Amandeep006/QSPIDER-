-- 1. WAQTD NUMBER OF EMPLOYEES GETTING SALARY LESS THAN 2000 IN DEPTNO 10.
SELECT COUNT(*) FROM EMP_TABLE
WHERE (SAL<2000 AND DEPTNO=10);

-- 2. WAQTD TOTAL SALARY NEEDED TO PAY EMPLOYEES WORKING AS CLERK.
SELECT SUM(SAL) FROM EMP_TABLE
WHERE JOB="CLERK";

-- 3. WAQTD AVERAGE SALARY NEEDED  TO PAY ALL EMPLOYEES.
SELECT AVG(SAL) AS AVERAGE_SALARY FROM EMP_TABLE;

-- 4. WAQTD NUMBER OF EMPLOYEES HAVING AS CLERK OR MANAGER.
SELECT COUNT(*) AS NO_OF_EMPLOYEES FROM EMP_TABLE
WHERE JOB IN ("CLERK", "MANAGER");

-- 5. WAQTD NUMBER OF EMPLOYEES HAVING "A" AS THEIR FIRST CHARACTER.
SELECT ENAME FROM EMP_TABLE
WHERE ENAME LIKE "A%";

-- 6. WAQTD TOTAL SALARY NEEDED TO PAY EMOPLOYEES HIRED IN FEB.
SELECT SUM(SAL) FROM EMP_TABLE
WHERE HIREDATE LIKE "%-FEB-%";

-- 7. WAQTD NUMBER OF EMPLOYEES REPORTING TO 7839 (MGR).
SELECT COUNT(*) FROM EMP_TABLE
WHERE MGR=7839;

-- 8. WAQTD NUMBER OF EMPLOYEES GETTING COMISSION IN DEPTNO. 30.
